package String_sec_2;

public class Q9 
{
	public static void main(String[] args) 
	{
		String s="Sathya";
		String str="";
		char ch[]=s.toCharArray();
		int i;
		
		for(i=0;i<ch.length;i++)
		{
			if(i==0 && ch[i]>=65 && ch[i]<=90)
			{
				ch[i]=(char)(ch[i]+32);
			}
			str=str+ch[i];
		}
		System.out.println(str);
	}
}
